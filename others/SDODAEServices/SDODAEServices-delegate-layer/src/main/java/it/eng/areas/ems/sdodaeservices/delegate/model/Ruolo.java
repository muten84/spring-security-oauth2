package it.eng.areas.ems.sdodaeservices.delegate.model;

public class Ruolo {

	private String id;

	private String nome;

	private String descrizione;

	private boolean globalSearch;

	private boolean globalSave;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public boolean isGlobalSearch() {
		return globalSearch;
	}

	public void setGlobalSearch(boolean globalSearch) {
		this.globalSearch = globalSearch;
	}

	public boolean isGlobalSave() {
		return globalSave;
	}

	public void setGlobalSave(boolean globalSave) {
		this.globalSave = globalSave;
	}

}
