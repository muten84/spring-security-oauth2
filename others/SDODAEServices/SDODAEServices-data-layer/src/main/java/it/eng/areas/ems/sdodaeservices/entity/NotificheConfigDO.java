package it.eng.areas.ems.sdodaeservices.entity;
import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;


@Entity
@Table(name = "DAE_NOTIFICHE_CONFIG")
public class NotificheConfigDO implements Serializable {

   @Id
   @Column(name= "ID")
	private String id;

	private boolean aggiungiDataCreazione;

	private TonoDO audioInterventoNuovo;

	private TonoDO audioInterventoAggiornato;

	private TonoDO audioInterventoAnnullato;

	private TonoDO audioInterventoTerminato;

	private TonoDO audioNews;

	private TonoDO audioCertificatoBLSDAE;

	
	public NotificheConfigDO() {
		super();
		// TODO Auto-generated constructor stub
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public boolean isAggiungiDataCreazione() {
		return aggiungiDataCreazione;
	}

	public void setAggiungiDataCreazione(boolean aggiungiDataCreazione) {
		this.aggiungiDataCreazione = aggiungiDataCreazione;
	}

	public TonoDO getAudioInterventoNuovo() {
		return audioInterventoNuovo;
	}

	public void setAudioInterventoNuovo(TonoDO audioInterventoNuovo) {
		this.audioInterventoNuovo = audioInterventoNuovo;
	}

	public TonoDO getAudioInterventoAggiornato() {
		return audioInterventoAggiornato;
	}

	public void setAudioInterventoAggiornato(TonoDO audioInterventoAggiornato) {
		this.audioInterventoAggiornato = audioInterventoAggiornato;
	}

	public TonoDO getAudioInterventoAnnullato() {
		return audioInterventoAnnullato;
	}

	public void setAudioInterventoAnnullato(TonoDO audioInterventoAnnullato) {
		this.audioInterventoAnnullato = audioInterventoAnnullato;
	}

	public TonoDO getAudioInterventoTerminato() {
		return audioInterventoTerminato;
	}

	public void setAudioInterventoTerminato(TonoDO audioInterventoTerminato) {
		this.audioInterventoTerminato = audioInterventoTerminato;
	}

	public TonoDO getAudioNews() {
		return audioNews;
	}

	public void setAudioNews(TonoDO audioNews) {
		this.audioNews = audioNews;
	}

	public TonoDO getAudioCertificatoBLSDAE() {
		return audioCertificatoBLSDAE;
	}

	public void setAudioCertificatoBLSDAE(TonoDO audioCertificatoBLSDAE) {
		this.audioCertificatoBLSDAE = audioCertificatoBLSDAE;
	}

	
	
}
