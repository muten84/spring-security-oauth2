package it.eng.areas.ems.sdodaeservices.entity.filter;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlType;

import it.eng.area118.sdocommon.dao.filter.EntityFilter;

@XmlType
@XmlAccessorType(XmlAccessType.FIELD)
public class ComuneFilterDO extends EntityFilter implements Serializable {

	/**
	 * 
	 */

	protected String id;

	protected String nomeComune;
	protected String codiceIstat;

	protected String nomeProvincia;

	public ComuneFilterDO() {
		super();
	}

	public ComuneFilterDO(String fetchrule) {
		super(fetchrule);
	}

	public String getNomeComune() {
		return nomeComune;
	}

	public void setNomeComune(String nomeComune) {
		this.nomeComune = nomeComune;
	}

	public String getCodiceIstat() {
		return codiceIstat;
	}

	public void setCodiceIstat(String codiceIstat) {
		this.codiceIstat = codiceIstat;
	}

	public String getId() {
		return id;
	}

	public String getNomeProvincia() {
		return nomeProvincia;
	}

	public void setNomeProvincia(String nomeProvincia) {
		this.nomeProvincia = nomeProvincia;
	}

	public void setId(String id) {
		this.id = id;
	}

}
