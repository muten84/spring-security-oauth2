package it.eng.areas.ems.sdodaeservices.entity;
import java.io.Serializable;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;


@Entity
@Table(name = "DAE_NOTIFICA")
public class NotificaDO implements Serializable {

	@Id
	@Column(name= "ID")
	private String id;
	
	private String alias;

	private String pnsIdentifier;
	
	private TonoDO tono;

	private String platform;

	private String messaggio;
	
	private String registrationId;

	private String tags;

	private Date validita;

	private FirstResponderDO proprieta;

	private Integer log;

	private Integer versione;

	private Date ultimaRichiesta;

	public NotificaDO() {
		super();
		// TODO Auto-generated constructor stub
	}

	
	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	public String getRegistrationId() {
		return registrationId;
	}


	public void setRegistrationId(String registrationId) {
		this.registrationId = registrationId;
	}


	public Date getUltimaRichiesta() {
		return ultimaRichiesta;
	}


	public void setUltimaRichiesta(Date ultimaRichiesta) {
		this.ultimaRichiesta = ultimaRichiesta;
	}


	public String getPnsIdentifier() {
		return pnsIdentifier;
	}

	public void setPnsIdentifier(String pnsIdentifier) {
		this.pnsIdentifier = pnsIdentifier;
	}

	public String getMessaggio() {
		return messaggio;
	}

	public void setMessaggio(String messaggio) {
		messaggio = messaggio;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public TonoDO getTono() {
		return tono;
	}

	public void setTono(TonoDO tono) {
		this.tono = tono;
	}

	public String getPlatform() {
		return platform;
	}

	public void setPlatform(String platform) {
		this.platform = platform;
	}

	public String getRegistrationID() {
		return registrationId;
	}

	public void setRegistrationID(String registrationID) {
		this.registrationId = registrationId;
	}

	public String getTags() {
		return tags;
	}

	public void setTags(String tags) {
		this.tags = tags;
	}

	public Date getValidita() {
		return validita;
	}

	public void setValidita(Date validita) {
		this.validita = validita;
	}

	public FirstResponderDO getProprieta() {
		return proprieta;
	}

	public void setProprieta(FirstResponderDO proprieta) {
		this.proprieta = proprieta;
	}

	public int getLog() {
		return log;
	}

	public void setLog(int log) {
		this.log = log;
	}

	public int getVersione() {
		return versione;
	}

	public void setVersione(int versione) {
		this.versione = versione;
	}

	public Date getultimaRichiesta() {
		return ultimaRichiesta;
	}

	public void setultimaRichiesta(Date ultimaRichiesta) {
		this.ultimaRichiesta = ultimaRichiesta;
	}

	
	
}
