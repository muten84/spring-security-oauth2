package it.eng.areas.ems.sdodaeservices.rest.model;

public class AvailabilityTimeRequest {

	private String from;

	private String to;

	public AvailabilityTimeRequest() {
		super();
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

}
