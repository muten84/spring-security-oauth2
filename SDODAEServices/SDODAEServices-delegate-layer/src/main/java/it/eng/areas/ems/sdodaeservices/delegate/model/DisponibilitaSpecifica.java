package it.eng.areas.ems.sdodaeservices.delegate.model;

import java.util.List;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
public class DisponibilitaSpecifica {

	private String id;

	private List<DisponibilitaGiornaliera> disponibilitaGiornaliera;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public List<DisponibilitaGiornaliera> getDisponibilitaGiornaliera() {
		return disponibilitaGiornaliera;
	}

	public void setDisponibilitaGiornaliera(List<DisponibilitaGiornaliera> disponibilitaGiornaliera) {
		this.disponibilitaGiornaliera = disponibilitaGiornaliera;
	}

}
