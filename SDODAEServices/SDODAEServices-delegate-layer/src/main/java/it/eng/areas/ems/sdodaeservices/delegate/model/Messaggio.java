package it.eng.areas.ems.sdodaeservices.delegate.model;

import java.util.Date;
import java.util.List;

public class Messaggio {

	private String id;

	private String testo;

	private Date invio;

	private List<FirstResponder> responders;

	private Integer destinatari;

	private Integer deleted = 0;
	
	private String stato;


	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTesto() {
		return testo;
	}

	public void setTesto(String testo) {
		this.testo = testo;
	}

	public Date getInvio() {
		return invio;
	}

	public void setInvio(Date invio) {
		this.invio = invio;
	}

	public List<FirstResponder> getResponders() {
		return responders;
	}

	public void setResponders(List<FirstResponder> responders) {
		this.responders = responders;
	}

	public Integer getDestinatari() {
		return destinatari;
	}

	public void setDestinatari(Integer destinatari) {
		this.destinatari = destinatari;
	}

	public Integer getDeleted() {
		return deleted;
	}

	public void setDeleted(Integer deleted) {
		this.deleted = deleted;
	}

	public String getStato() {
		return stato;
	}

	public void setStato(String stato) {
		this.stato = stato;
	}
	
	
	

}
