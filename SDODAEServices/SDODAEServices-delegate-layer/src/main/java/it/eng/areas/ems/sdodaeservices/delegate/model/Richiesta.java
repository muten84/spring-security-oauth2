package it.eng.areas.ems.sdodaeservices.delegate.model;
import java.util.Date;

public class Richiesta {

	private Integer codiceRichiesta;

	private Date data;

	private GPSLocation luogo;

	private String nota;

	private FirstResponder apertoDa;

	private StatoRichiesta statoRichiesta;

	private Provincia[] inviaAlertA;

	private Integer tempoMinimoAlert;

	public int getCodiceRichiesta() {
		return codiceRichiesta;
	}

	public void setCodiceRichiesta(int codiceRichiesta) {
		this.codiceRichiesta = codiceRichiesta;
	}

	public Date getData() {
		return data;
	}

	public void setData(Date data) {
		this.data = data;
	}

	public GPSLocation getLuogo() {
		return luogo;
	}

	public void setLuogo(GPSLocation luogo) {
		this.luogo = luogo;
	}

	public String getNota() {
		return nota;
	}

	public void setNota(String nota) {
		this.nota = nota;
	}

	public FirstResponder getApertoDa() {
		return apertoDa;
	}

	public void setApertoDa(FirstResponder apertoDa) {
		this.apertoDa = apertoDa;
	}

	public StatoRichiesta getStatoRichiesta() {
		return statoRichiesta;
	}

	public void setStatoRichiesta(StatoRichiesta statoRichiesta) {
		this.statoRichiesta = statoRichiesta;
	}

	public Provincia[] getInviaAlertA() {
		return inviaAlertA;
	}

	public void setInviaAlertA(Provincia[] inviaAlertA) {
		this.inviaAlertA = inviaAlertA;
	}

	public int getTempoMinimoAlert() {
		return tempoMinimoAlert;
	}

	public void setTempoMinimoAlert(int tempoMinimoAlert) {
		this.tempoMinimoAlert = tempoMinimoAlert;
	}


	

}
